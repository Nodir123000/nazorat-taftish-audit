#!/bin/bash

# Скрипт подготовки финального образа АИС КРР (9 подсистем)
# Ревизия: Golden Image v2.0
# Дата: 20.04.2026

set -e # Остановка при ошибке

echo "--------------------------------------------------"
echo "🚀 Финализация АИС КРР (ERP Upgrade Phase)"
echo "--------------------------------------------------"

# 1. Приведение зависимостей в актуальное состояние
echo "[1/5] Установка зависимостей (mathjs, ioredis, aws-sdk)..."
npm install --legacy-peer-deps

# 2. Подготовка Prisma
echo "[2/5] Генерация Prisma Client и типов..."
npx prisma generate

# 3. Синхронизация БД
echo "[3/5] Применение миграций ERP (erp_upgrade_v2)..."
# В реальной среде используется migrate deploy для безопасности данных
npx prisma migrate deploy

# 4. Проверка целостности кода
echo "[4/5] Валидация сборки..."
npm run build

# 5. Контейнеризация
echo "[5/5] Сборка Docker-образа (standalone)..."
docker build -t ais-krr-erp:latest .

echo "--------------------------------------------------"
echo "✅ ПРОЦЕСС ЗАВЕРШЕН УСПЕШНО"
echo "Образ 'ais-krr-erp:latest' готов к развертыванию."
echo "--------------------------------------------------"
