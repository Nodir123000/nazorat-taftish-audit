async function main() {
  console.log('Cleaning ref_violations table...')
  await prisma.ref_violations.deleteMany({})

  console.log('Seeding ref_violations table...')
  
  let codeCounter = 1;

  for (const section of violationsData) {
    for (const kind of section.kinds) {
      for (const detail of kind.details) {
        await prisma.ref_violations.create({
          data: {
            code: `V_${codeCounter.toString().padStart(4, '0')}`,
            category: section.category, // Раздел
            description: kind.severity, // Вид
            name: { ru: detail },      // Детализация
            legal_basis: "Приказ МО №600"
          }
        })
        codeCounter++;
      }
    }
  }

  console.log(`Successfully seeded ${codeCounter - 1} violation details into the database.`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
